# Dictionaries in python

my_dict = {'apple' : 3, 'banana' : 5, 'orange' : 2}

print(my_dict['apple'])  # Output:  3

my_dict['grapes'] = 4  # Change the value of 'banana'
print(my_dict)  # Output: {'apple': 3, 'banana': 5, 'orange': 2, 'grapes': 4}

my_dict['banana'] = 7 # Change the value of 'banana'
print(my_dict)  # Output: {'apple': 3, 'banana': 7, 'orange': 2, 'grapes': 4}