#Tuples

my_tuple = (1, 2, 3, 4, 5)
print(my_tuple)  # Access the first item
print(my_tuple[0])  # Output: 1
print(my_tuple[0])  # Output: 1
print(my_tuple[-1])  # Output: 5
print(my_tuple[-2])  # Output: 4

# Tuples are immutable, so you cannot change their items

tuple1 = (1, 2, 3)
tuple2 = (4, 5, 6)

# Concatenating tuples
conc_tuple = tuple1 + tuple2
rep_tuple = tuple1 * 3
print(conc_tuple)  # Output: (1, 2, 3, 4, 5, 6)
print(rep_tuple)  # Output: (1, 2, 3, 1, 2, 3, 1, 2, 3)





